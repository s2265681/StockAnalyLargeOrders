def _fmt_amount(val) -> str:
    v = float(val or 0)
    if abs(v) >= 1e8:
        return f"{v/1e8:.2f}亿"
    if abs(v) >= 1e4:
        return f"{v/1e4:.0f}万"
    return f"{v:.0f}元"


def _seat_lines(seats, label: str) -> str:
    if not seats:
        return f"{label}：无数据\n"
    lines_out = [f"{label}："]
    for s in seats:
        hm = "【游资】" if s.get("is_hot_money") else ""
        lines_out.append(
            f"  {s['rank_no']}. {s['seat_name']}{hm}"
            f"  买入{_fmt_amount(s['buy_amount'])} 卖出{_fmt_amount(s['sell_amount'])} 净额{_fmt_amount(s['net_amount'])}"
        )
    return "\n".join(lines_out) + "\n"


